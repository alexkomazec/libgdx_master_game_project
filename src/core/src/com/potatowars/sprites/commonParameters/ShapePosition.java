package com.potatowars.sprites.commonParameters;

public class ShapePosition {

    //2D position
    private float x,y;

    public ShapePosition(float x, float y){
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }

}
